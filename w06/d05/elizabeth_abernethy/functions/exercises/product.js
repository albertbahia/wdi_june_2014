// Write a function to accept three numbers and return their product.

var product = function(num1, num2, num3) {
  return num1 * num2 * num3;
};

var answer = product(2, 4, 6);
console.log(answer);

var answer1 = product(1, 2, 3);
console.log(answer1);

var answer2 = product(234, 27835, 853);
console.log(answer2);


// alternative
console.log(product(4, 5, 6));
