class Apartment
  def initialize(initial_floor, initial_name, initial_price, initial_sqft, initial_num_bedrooms, initial_num_bathrooms)
    @floor = initial_floor
    @name = initial_name
    @price = initial_price
    @sqft = initial_sqft
    @num_bedrooms = initial_num_bedrooms
    @num_bathrooms = initial_num_bathrooms
  end
end
